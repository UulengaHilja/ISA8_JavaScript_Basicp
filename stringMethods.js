// declare a variable name : fullName then assign your name to its value.
const fullName = "Hilja Uulenga";


// using string method to get the length of variable fullName,
const fullNameLength = fullName.length;
console.log(fullNameLength);

// using string method to uppercase the variable
const fullNameUpperCase = fullName.toUpperCase();
console.log(fullNameUpperCase);

// using string method to lowercase the variable
const fullNameLowerCase = fullName.toLowerCase();
console.log(fullNameLowerCase); 

// using string method to slice the variable
const fullNameSlice = fullName.slice(0, 4);
console.log(fullNameSlice);

// using string method to check which letter has index 3
const fullNameChar = fullName.charAt(3);
console.log(fullNameChar); 

// using string method to get the letter has index between 1 and 3
const fullNameSubstring = fullName.substring(1, 4);
console.log(fullNameSubstring); 